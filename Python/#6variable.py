#Variable is a container which contains some information.
var1 = "Hello " #string (str)
var2 = 45 #integer (int)
var3 = 36.7 #floating point (float)
var4 = "Shivanshu"
#Type function can be used to identify the type of information stored in a variable.
print(type(var1))
print(type(var2))
print(type(var3))
'''
Variables can be printed like this anywhere in the program. And, you can also do some mathematical operations
with their help
'''
print(var1)
print(var2)
print(var3)
print(var2 + var3)
#Two strings can also be added
print(var1 + var4)
